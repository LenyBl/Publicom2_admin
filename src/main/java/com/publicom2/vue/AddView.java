    private void btnValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValiderActionPerformed
        //on r√©cup√®re le nom saisi par l'utilisateur
        String name = this.getNom();
        //R√©cup√®re le prenom du nouvel utilisateur
        String firstName = this.getPrenom();
        //Cr√©er l'identifiant du nouvel utilisateur sous le format : nom.prenom
        String username = firstName + "." + name;
        //R√©cup√®re le mot de passe du nouvel utilisateur
        String password = this.getMdp();
        //Cr√©ation d'un objet Utilisateur repr√©sentant le nouvel utilisateur
        //Avec le nouvel identifiant, nom, prenom et mot de passe
        Utilisateur user = new Utilisateur(username, password, name, firstName);
        //Instance du controlleur
        AddViewController controller = new AddViewController(this);
        //Demande au controlleur d'ajouter l'utilisateur
        try {
            controller.addUser(user);
        } catch (Exception ex) {
            Logger.getLogger(AddView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnValiderActionPerformed

